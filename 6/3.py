"""
Исполнитель Черепаха действует на плоскости с декартовой
системой координат. В начальный момент Черепаха находится в
начале координат, её голова направлена вдоль положительного
направления оси ординат, хвост опущен. При опущенном хвосте
Черепаха оставляет на поле след в виде линии. В каждый
конкретный момент известно положение исполнителя и
направление его движения. У исполнителя существует
две команды:

    Вперёд n (где n – целое число), вызывающая передвижение
    Черепахи на n единиц в том направлении, куда указывает её голова,

    Направо m (где m – целое число), вызывающая изменение
    направления движения на m градусов по часовой стрелке.

Запись Повтори k [Команда1 Команда2 … КомандаS] означает,
что последовательность из S команд повторится k раз.

Черепахе был дан для исполнения следующий алгоритм:

Повтори 11 [Вперёд 4 Направо 60]

Определите, сколько точек с целочисленными положительными
координатами будут находиться внутри области, ограниченной
линией, заданной данным алгоритмом. Точки на линии
учитывать не следует.
"""
from turtle import *
r = 50
tracer(0)

home()
seth(90)
begin_fill()
for i in range(6):
    fd(4*r)
    rt(60)
end_fill()

up()
c = 0
canvas = getcanvas()
for x in range(0, 20):
    for y in range(-20, 0):
        a = canvas.find_overlapping(x* r, y * r, x * r, y * r)
        if a == (5,):
            c+=1
            print(a,c)
update()
mainloop()