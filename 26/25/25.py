"""
Есть весы и некоторый набор гирь. Каждая гиря имеет целочисленный вес.
Весовщику стало интересно, любой ли вес можно получить, используя только
имеющиеся гири. Среди значений меньших общего веса всех гирь, найдите
количество целых значений весов, которые невозможно набрать, используя
только имеющиеся гири, а также максимальный из них.

Входные данные

В первой строке входного файла находится число N - количество гирь
(натуральное число, не превышающее 10 000). В следующих N строках
находятся веса отдельных гирь (все числа натуральные, не превышающие
1 000 000), каждый в отдельной строке. Общий вес всех гирь не превышает
10 000 000.

Запишите в ответе два числа: количество целых значений весов, меньших
веса всех гирь, которые невозможно набрать, используя только имеющиеся гири,
а также максимальный из них.

Пример входного файла

5
7
1
3
14
1

При таких исходных данных нельзя набрать веса 6, 13, 20.
Поэтому ответ для приведённого примера: 3 20
"""
f = open('25.txt')
a = [int(q) for q in f][1:]
b = [0]*(sum(a)+1)
s = 0
for x in a:
    b2 = b.copy()
    for i in range(s+1):
        if b[i] == 1:
            b2[i+x] = 1
    b2[x] = 1
    b = b2
    s+=x
c = b[::-1].index(0)
b = ''.join(''.join(map(str,b)).split('1'))
print(len(b), c)
